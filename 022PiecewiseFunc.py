# 022:分段函数
# 查看提交统计提问
# 总时间限制: 1000ms 内存限制: 65536kB
# 描述
# 编写程序，计算下列分段函数y=f(x)的值。
#
# y=-x+2.5; 0 <= x < 5
#
# y=2-1.5(x-3)(x-3); 5 <= x < 10
#
# y=x/2-1.5; 10 <= x < 20
#
# 输入
# 一个浮点数N,0 <= N < 20
# 输出
# 输出N对应的分段函数值：f(N)。结果保留到小数点后三位。
# 样例输入
# 1.0
# 样例输出
# 1.500

x = float(input())
fx = x
if 0 <= x < 5:
    fx = -x + 2.5
elif 5 <= x < 10:
    fx = 2 - 1.5 * (x - 3) * (x - 3)
elif 10 <= x < 20:
    fx = x / 2 - 1.5
print(f'{fx:.3f}')
